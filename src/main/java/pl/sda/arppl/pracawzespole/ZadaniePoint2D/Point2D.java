package pl.sda.arppl.pracawzespole.ZadaniePoint2D;

public class Point2D {
    int x1;
    int x2;

    public Point2D(int x1, int x2) {
        this.x1 = x1;
        this.x2 = x2;
    }

    public void setX1(int x1) {
        this.x1 = x1;
    }

    public void setX2(int x2) {
        this.x2 = x2;
    }

    public int getX1() {
        return x1;
    }

    public int getX2() {
        return x2;
    }
}
